const getSpace = (height=30) => {
    return`
    <tr>
        <td height="${height}"></td>
    </tr>
    `
}

const getImage = (src, href=null)=>{
    if(href){
        return `
            <tr>
                <td align="center" style="height: 30px"><a
                        href="${href}" target="_blanc"><img
                        width="550px" style="border-radius: 8px;"
                        src="${src}" /></a>
                </td>
            </tr>
            ${getSpace()}
            `
    }else{
        return `
        <tr>
            <td align="center" style="height: 30px">
                <img width="550px" style="border-radius: 8px;" src="${src}" />
            </td>
        </tr>
        ${getSpace()}
        `
    }   
}

